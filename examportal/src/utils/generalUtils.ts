export const validateDate =(value:any)=>{
    const dateformat = /^(0?[1-9]|1[0-2])\/(0?[1-9]|1\d|2\d|3[01])\/(19|20)\d{2}$/;
    if(value?.match(dateformat)){
      return true
    }else{
      return false
    }
  }
